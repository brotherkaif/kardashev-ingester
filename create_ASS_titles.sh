ScriptType: v4.00+
WrapStyle: 0
ScaledBorderAndShadow: yes
YCbCr Matrix: None

[V4+ Styles]
Format: Name, Fontname, Fontsize, PrimaryColour, SecondaryColour, OutlineColour, BackColour, Bold, Italic, Underline, StrikeOut, ScaleX, ScaleY, Spacing, Angle, BorderStyle, Outline, Shadow, Alignment, MarginL, MarginR, MarginV, Encoding
Style: Default,Arial,10,&H00FFFFFF,&H000000FF,&H00000000,&H00000000,0,0,0,0,100,100,0,0,3,2,0,1,10,10,10,1

[Events]
Format: Layer, Start, End, Style, Name, MarginL, MarginR, MarginV, Effect, Text
Dialogue: 0,$TC_AUDIO_INTRO_TITLE_START.00,$TC_AUDIO_INTRO_TITLE_END.00,Default,,0,0,0,,[AUDIO]\N"$( jq -r '.file.audio_stream.title' $INPUT_DIRECTORY/$INPUT_METADATA )" / $( jq -r '.file.audio_stream.author' $INPUT_DIRECTORY/$INPUT_METADATA ) / $( jq -r '.file.audio_stream.licence' $INPUT_DIRECTORY/$INPUT_METADATA )
Dialogue: 0,$TC_VIDEO_INTRO_TITLE_START.00,$TC_VIDEO_INTRO_TITLE_END.00,Default,,0,0,0,,[VIDEO]\N"$( jq -r '.file.video_stream.title' $INPUT_DIRECTORY/$INPUT_METADATA )" / $( jq -r '.file.video_stream.author' $INPUT_DIRECTORY/$INPUT_METADATA ) / $( jq -r '.file.video_stream.licence' $INPUT_DIRECTORY/$INPUT_METADATA )
Dialogue: 0,$TC_AUDIO_OUTRO_TITLE_START.00,$TC_AUDIO_OUTRO_TITLE_END.00,Default,,0,0,0,,[AUDIO]\N"$( jq -r '.file.audio_stream.title' $INPUT_DIRECTORY/$INPUT_METADATA )" / $( jq -r '.file.audio_stream.author' $INPUT_DIRECTORY/$INPUT_METADATA ) / $( jq -r '.file.audio_stream.licence' $INPUT_DIRECTORY/$INPUT_METADATA )
Dialogue: 0,$TC_VIDEO_OUTRO_TITLE_START.00,$TC_VIDEO_OUTRO_TITLE_END.00,Default,,0,0,0,,[VIDEO]\N"$( jq -r '.file.video_stream.title' $INPUT_DIRECTORY/$INPUT_METADATA )" / $( jq -r '.file.video_stream.author' $INPUT_DIRECTORY/$INPUT_METADATA ) / $( jq -r '.file.video_stream.licence' $INPUT_DIRECTORY/$INPUT_METADATA )
EOF
